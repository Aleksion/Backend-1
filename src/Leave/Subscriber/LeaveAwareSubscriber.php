<?php

namespace Persona\Hris\Leave\Subscriber;

use Doctrine\Common\EventSubscriber;
use Doctrine\ORM\Event\LifecycleEventArgs;
use Doctrine\ORM\Events;
use Persona\Hris\Leave\Model\LeaveAwareInterface;
use Persona\Hris\Leave\Model\LeaveInterface;
use Persona\Hris\Leave\Model\LeaveRepositoryInterface;
use Symfony\Component\HttpKernel\Exception\NotFoundHttpException;

/**
 * @author Muhamad Surya Iksanudin <surya.iksanudin@personahris.com>
 */
final class LeaveAwareSubscriber implements EventSubscriber
{
    /**
     * @var LeaveRepositoryInterface
     */
    private $repository;

    /**
     * @param LeaveRepositoryInterface $repository
     */
    public function __construct(LeaveRepositoryInterface $repository)
    {
        $this->repository = $repository;
    }

    /**
     * @param LifecycleEventArgs $eventArgs
     */
    public function prePersist(LifecycleEventArgs $eventArgs)
    {
        $entity = $eventArgs->getEntity();
        if ($entity instanceof LeaveAwareInterface) {
            $this->isValidOrException($entity->getLeaveId());
        }
    }

    /**
     * @param LifecycleEventArgs $eventArgs
     */
    public function preUpdate(LifecycleEventArgs $eventArgs)
    {
        $entity = $eventArgs->getEntity();
        if ($entity instanceof LeaveAwareInterface) {
            $this->isValidOrException($entity->getLeaveId());
        }
    }

    /**
     * @param LifecycleEventArgs $eventArgs
     */
    public function postLoad(LifecycleEventArgs $eventArgs)
    {
        $entity = $eventArgs->getEntity();
        if ($entity instanceof LeaveAwareInterface) {
            $this->repository->find($entity->getLeaveId());
        }
    }

    /**
     * @param string $id
     *
     * @return bool|null
     */
    private function isValidOrException(string $id): ? bool
    {
        if (!$this->repository->find($id) instanceof LeaveInterface) {
            throw new NotFoundHttpException(sprintf('Leave with id %s is not found.', $id));
        }

        return true;
    }

    /**
     * @return array
     */
    public function getSubscribedEvents(): array
    {
        return [Events::prePersist, Events::preUpdate, Events::postLoad];
    }
}
